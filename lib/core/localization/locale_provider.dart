import '../services/storage/storage_service.dart';

enum AppLanguage {
  en('English', 'en'),
  ja('日本語', 'ja'),
  ta('தமிழ்', 'ta'),
  es('Español', 'es'),
  pl('Polski', 'pl'),
  zh('中文', 'zh'),
  ko('한국어', 'ko'),
  tl('Tagalog', 'tl');

  const AppLanguage(this.displayName, this.code);
  final String displayName;
  final String code;

  static AppLanguage fromCode(String code) {
    return AppLanguage.values.firstWhere(
      (l) => l.code == code,
      orElse: () => AppLanguage.en,
    );
  }
}

class LocaleNotifier extends Notifier<AppLanguage> {
  static const _langKey = 'mei_selected_language';

  @override
  AppLanguage build() {
    final stored = StorageService.getString(_langKey);
    if (stored != null) {
      return AppLanguage.fromCode(stored);
    }
    // Fallback to system locale if supported, otherwise English
    final systemLocale = WidgetsBinding.instance.platformDispatcher.locale.languageCode;
    if (systemLocale == 'ja') {
      return AppLanguage.ja;
    } else if (systemLocale == 'ta') {
      return AppLanguage.ta;
    } else if (systemLocale == 'es') {
      return AppLanguage.es;
    } else if (systemLocale == 'pl') {
      return AppLanguage.pl;
    } else if (systemLocale == 'zh') {
      return AppLanguage.zh;
    } else if (systemLocale == 'ko') {
      return AppLanguage.ko;
    } else if (systemLocale == 'tl') {
      return AppLanguage.tl;
    }
    return AppLanguage.en;
  }

  Future<void> setLanguage(AppLanguage lang) async {
    state = lang;
    await StorageService.setString(_langKey, lang.code);
  }
}

final localeProvider = NotifierProvider<LocaleNotifier, AppLanguage>(LocaleNotifier.new);

final translationsProvider = Provider<Map<String, String>>((ref) {
  final lang = ref.watch(localeProvider);
  return _translations[lang] ?? _translations[AppLanguage.en]!;
});

// Extension to make getting translations from ref easy
extension RefTranslate on WidgetRef {
  String tr(String key) {
    final map = watch(translationsProvider);
    return map[key] ?? key;
  }
}

